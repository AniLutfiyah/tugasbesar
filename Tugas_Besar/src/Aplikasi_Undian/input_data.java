    private void tampilkan() {
        int row = Tabel.getRowCount();
        for (int a= 0; a<row;a++){
            model.removeRow(0);
        }
        try {
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/random_name","root","");
        ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM random");
        while(rs.next()){
            String data []= {rs.getString(1),rs.getString(2)};
            model.addRow(data);
        }
        } catch (SQLException ex) {
            Logger.getLogger(input_data.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
